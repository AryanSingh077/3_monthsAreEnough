# Arithmetic Operations (except complex where noted)

# + , - , * , / → normal arithmetic

# // → floor division (rounds down)

# % → remainder

# ** or pow() → power

# abs() → absolute value (magnitude for complex)

# divmod(x, y) → (x//y, x%y)

# Special points:

# // always rounds towards −∞

# int(float) truncates decimal part

# 0 ** 0 = 1 in Python